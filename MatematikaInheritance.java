public class MatematikaInheritance {

  public static void main(String[] args) {
    Matematika2 m2 = new Matematika2();

    System.out.println(m2.penjumlahan(10, 20));
    System.out.println(m2.pengurangan(100, 50));
    System.out.println(m2.perkalian(10, 5));
    System.out.println(m2.pembagian(100, 25));
    System.out.println(m2.modulus(10, 5));
  }
}
